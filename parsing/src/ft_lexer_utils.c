#include "../mini.h"

int	ft_check_case_01(char c)
{
	if (c == '<' || c == '>' || c == '|' || c == ' ' || c == '\t')
		return (1);
	else
		return (0);
}

int	ft_check_case_02(char c)
{
	if ((c == '\0' || c == ' ' || c == '\t'))
		return (1);
	else
		return (0);
}
